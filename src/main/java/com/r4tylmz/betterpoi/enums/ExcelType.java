package com.r4tylmz.betterpoi.enums;

/**
 * Enum representing the types of Excel files.
 * This enum provides constants for the two main types of Excel files: XLSX and XLS.
 */
public enum ExcelType {
    /**
     * Constant for Excel files with the .xlsx extension.
     */
    XLSX,

    /**
     * Constant for Excel files with the .xls extension.
     */
    XLS
}